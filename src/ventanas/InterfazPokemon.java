    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        int level = (int) Math.floor(Math.random() * (sliderMax.getValue() - sliderMin.getValue() + 1) + sliderMin.getValue());
        txtNivel.setText(String.valueOf(level));
        
        int points = level + 5;
        txtVelocidad.setText(String.valueOf(points));
        int aleatorio = (int) Math.floor(Math.random() * (points / 2) + 1);
        int aleatorio2 = (int) Math.floor(Math.random() * (points / 2) + 1);
        ArrayList<Integer> numbers = new ArrayList<Integer>();
        numbers.add(aleatorio);
        numbers.add(aleatorio2);
        numbers.add(points - (numbers.get(0) + numbers.get(1)));
        Collections.shuffle(numbers);
        txtAtaque.setText("" + numbers.get(0));
        txtDefensa.setText("" + numbers.get(1));
        txtEspecial.setText("" + numbers.get(2));
    }//GEN-LAST:event_btnGenerarActionPerformed

